#changing case of a string

statement=input("enter the statement here")

#uppercase
print("-----------------------------")
print(statement.upper())

#lowercase
print("-------------------------------")
print(statement.lower())

#swapcase
print("--------------------")
print(statement.swapcase()) 

#title
print("---------------------")
print(statement.title())

#capitalize
print("------------------------")
print(statement.capitalize())

